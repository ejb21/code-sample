// This program will show how to declare, assign, and initialize variables

public class Initialize {

	public static void main(String[] args) {
		
		//Declare variable
		char firstInitial;
		
		//Assign variable
		firstInitial = 'E';
		
		//Print variable
		System.out.println(firstInitial);
		
		//Initialize a variable
		int birthMonth = 8;
		
		// Print
		System.out.println(birthMonth);
		
		// Build string with multiple variables
		System.out.println(firstInitial + " was born on month " + birthMonth);

	}

}
